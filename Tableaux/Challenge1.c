#include <stdio.h>

int main() {
    int tableau[5] = {1, 2, 3,4,5};

    // Affichage 
    printf("%d\n", tableau[0]);
    printf("%d\n", tableau[1]);
    printf("%d\n", tableau[2]);
    printf("%d\n", tableau[3]);
    printf("%d\n", tableau[4]);

    // Affichage  boucle
    printf("Contenu du tableau :\n");
    for (int i = 0; i < 5; i++) {
        printf("%d\n", tableau[i]);
    }

    return 0;
}



    

 

